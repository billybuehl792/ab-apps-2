export const RegexPattern = {
  Email:
    /^(([^<>()[\]\\.,;:\s@"]+(\.[^<>()[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/,
  Phone: /^[+]?(?:(\d{1}))?[\s]?[(]?(\d{3})[)]?[-|\s]?(\d{3})[-|\s]?(\d{4})$/,
  ZipCode: /^\d{5}(?:[-\s]\d{4})?$/,
};
