export const RegexPattern = {
  EMAIL:
    /^(([^<>()[\]\\.,;:\s@"]+(\.[^<>()[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/,
  PHONE: /^[+]?(?:(\d{1}))?[\s]?[(]?(\d{3})[)]?[-|\s]?(\d{3})[-|\s]?(\d{4})$/,
  ZIP_CODE: /^\d{5}(?:[-\s]\d{4})?$/,
};
